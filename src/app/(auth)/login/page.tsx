
"use client";

import { useMemo } from "react";
import { useRouter } from "next/navigation";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { z } from "zod";
import { Button } from "@/components/ui/button";
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from "@/components/ui/form";
import { useToast } from "@/hooks/use-toast";
import { useAuth, useUser, useFirestore, useDoc, useMemoFirebase, initiateEmailSignIn } from "@/firebase";
import { doc } from 'firebase/firestore';
import type { GlobalConfig } from "@/models/global-config";
import { useEffect } from "react";
import Link from "next/link";
import { Loader2 } from "lucide-react";

const SUPER_ADMIN_EMAIL = "allseosoporte@gmail.com";

const loginSchema = z.object({
  email: z.string().email({ message: "Introduce un correo electrónico válido." }),
  password: z.string().min(1, { message: "La contraseña es obligatoria." }),
});

export default function LoginPage() {
  const router = useRouter();
  const { toast } = useToast();
  const auth = useAuth();
  const firestore = useFirestore();
  const { user, isUserLoading } = useUser();

  const configDocRef = useMemoFirebase(() => {
    if (!firestore) return null;
    return doc(firestore, 'globalConfig', 'system');
  }, [firestore]);

  const { data: config, isLoading: isConfigLoading } = useDoc<GlobalConfig>(configDocRef);

  const form = useForm<z.infer<typeof loginSchema>>({
    resolver: zodResolver(loginSchema),
    defaultValues: {
      email: "",
      password: "",
    },
  });

  useEffect(() => {
    if (!isUserLoading && user) {
      if (user.email === SUPER_ADMIN_EMAIL) {
        router.push("/superadmin");
      } else {
        router.push("/dashboard");
      }
    }
  }, [user, isUserLoading, router]);

  async function onSubmit(values: z.infer<typeof loginSchema>) {
    if (!auth) return;
    try {
      await initiateEmailSignIn(auth, values.email, values.password);
      toast({
        title: "Inicio de sesión exitoso",
        description: "Serás redirigido a tu panel.",
      });
      // The useEffect will handle the redirection.
    } catch (error: any) {
      toast({
        variant: "destructive",
        title: "Error de Autenticación",
        description: error.message || "No se pudo iniciar sesión. Verifica tus credenciales.",
      });
    }
  }

  const isLoading = isUserLoading || isConfigLoading;

  if (isLoading || user) {
    return (
      <div className="flex justify-center items-center h-screen">
        <div className="text-center flex flex-col items-center gap-2">
          <Loader2 className="h-8 w-8 animate-spin text-primary" />
          <p className="text-muted-foreground">Cargando y verificando sesión...</p>
        </div>
      </div>
    );
  }

  return (
    <Card>
      <CardHeader className="space-y-1 text-center">
        <CardTitle className="text-2xl font-headline">Acceder a tu Cuenta</CardTitle>
        <CardDescription>
          Introduce tu correo y contraseña para continuar.
        </CardDescription>
      </CardHeader>
      <Form {...form}>
        <form onSubmit={form.handleSubmit(onSubmit)}>
          <CardContent className="grid gap-4">
            <FormField
              control={form.control}
              name="email"
              render={({ field }) => (
                <FormItem>
                  <FormLabel>Correo Electrónico</FormLabel>
                  <FormControl>
                    <Input placeholder="nombre@ejemplo.com" {...field} />
                  </FormControl>
                  <FormMessage />
                </FormItem>
              )}
            />
            <FormField
              control={form.control}
              name="password"
              render={({ field }) => (
                <FormItem>
                  <FormLabel>Contraseña</FormLabel>
                  <FormControl>
                    <Input type="password" placeholder="Tu contraseña" {...field} />
                  </FormControl>
                  <FormMessage />
                </FormItem>
              )}
            />
          </CardContent>
          <CardFooter className="flex flex-col gap-4">
            <Button className="w-full" type="submit" disabled={form.formState.isSubmitting}>
              {form.formState.isSubmitting ? "Accediendo..." : "Acceder"}
            </Button>
            {config?.allowUserRegistration && (
              <div className="text-sm text-muted-foreground">
                ¿No tienes una cuenta?{" "}
                <Link href="/register" className="underline text-primary hover:text-primary/80">
                  Regístrate aquí
                </Link>
                .
              </div>
            )}
          </CardFooter>
        </form>
      </Form>
    </Card>
  );
}
